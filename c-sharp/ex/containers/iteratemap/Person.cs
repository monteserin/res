using System;
using System.Collections;
using System.Collections.Generic;
using System.Text;

namespace IterateMap
{
  class Person
  {
    private int _age;
    private string _name;
    private string _dni;

    public int age
    {
      get { return _age; }
      set { _age = value; }
    }

    public string name
    {
      get { return _name; }
      set { _name = value; }
    }

    public string dni
    {
      get { return dni; }
      set { _dni = value; }
    }

  }
}
